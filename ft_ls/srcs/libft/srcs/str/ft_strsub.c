#include "libft.h"

char	*ft_strsub(const char *s, unsigned int start, size_t len)
{
	char			*substr;
	unsigned int	i;

	if (!s || !(*s) || !(substr = (char *)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	i = 0;
	while (len--)
	{
		substr[i] = s[start + i];
		i++;
	}
	substr[i] = '\0';
	return (substr);
}
