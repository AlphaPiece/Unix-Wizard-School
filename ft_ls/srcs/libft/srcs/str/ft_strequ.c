#include "libft.h"

t_bool	ft_strequ(const char *s1, const char *s2)
{
	if (!s1 || !s2 || *s1 != *s2)
		return (false);
	if (!(*s1) && !(*s2))
		return (true);
	return (ft_strequ(s1 + 1, s2 + 1));
}
