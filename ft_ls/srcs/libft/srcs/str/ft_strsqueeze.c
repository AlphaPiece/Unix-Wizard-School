/*
** precondition: str is an array of characters
*/

void	ft_strsqueeze(char s[], int c)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (s[i])
	{
		if (s[i] != c)
			s[j++] = s[i];
		i++;
	}
	s[j] = '\0';
}
